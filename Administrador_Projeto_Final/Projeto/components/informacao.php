            <p>



                No piso térreo do convento podem ver-se o coro baixo, com o túmulo de Santa Joana Princesa; a Igreja de
                Jesus; o claustro, com as suas capelas, sala do capítulo e refeitório.

                <br>

                De destacar a Igreja de Jesus, decorada com uma sumptuosa talha dourada e azulejos portugueses. Esta
                igreja é um notável exemplo da exuberância da decoração barroca.
                A capela maior foi desenhada pelos entalhadores e escultores António Gomes e José Correia e pelo pintor
                Manuel Ferreira e Sousa.

                <br>

                O coro interior que contem o túmulo da Princesa Joana é uma obra-prima da arte barroca em mármore
                multicor com elementos escultóricos influenciados pelo gosto italiano, sendo da autoria do arquitecto
                real João Antunes.

                <br>

                O claustro que remonta ao final do séc. XV e início do séc. XVI, bem como o refeitório, com a sua
                graciosa tribuna de leitura e paredes revestidas de azulejos são igualmente dignas de destaque.

                <br>

                No piso superior, o claustro dá acesso ao coro alto e às capelas devocionais que lhe são contíguas.

                <br>

                No espaço da exposição permanente, surgem ainda dois vestígios do antigo convento: a Sala de Lavor e a
                Capela do Senhor dos Passos.

                <br>

                Visite o Museu de Aveiro, que alberga um espólio significativo de obras de pintura, escultura, talha,
                azulejo ou ourivesaria, ao mesmo tempo que percorre o silêncio do espaço dominicano, por entre as
                arcadas do claustro, o contraste de cores dos altares da igreja, ou as pedras solenes do dormitório.

