            <p>

                O Museu de Aveiro está instalado no antigo Convento de Jesus da Ordem Dominicana feminina. Este
                convento, um dos mais antigos de Aveiro, remonta à 2ª metade do séc. XV, fundado por D. Brites Leitão e
                por D. Mécia Pereira.

                <br>

                A princesa D. Joana, filha de Afonso V, entra nesta casa em 1472. Aqui leva uma vida de santidade, o que
                conduz à sua beatificação em 1693. O prestígio do convento ficou assim definitivamente associado à
                presença da Princesa Santa Joana e ao seu culto religioso.

                <br>

                Do séc. XVI ao séc. XVIII, o edifício sofreu inúmeras obras de ampliação, melhoramentos e enriquecimento
                artístico. O séc. XIX é marcado pelos ideais liberais e suas amplas repercussões na vida religiosa. Por
                decreto de 1834, o ministro Joaquim António de Aguiar determina a extinção de ordens religiosas no
                reino. A sua aplicação é imediata nos conventos masculinos, permitindo, porém, a vida de clausura nos
                conventos femininos até à data da morte da última freira. Assim, no Convento de Jesus, a clausura
                mantém-se até 1874.

                <br>

                Em 1882, o edifício é entregue à Ordem Terceira Dominicana, que o transforma em Colégio de Santa Joana.
                Com a implantação da República, em 1910, e o agravamento da intolerância religiosa, a comunidade do
                Colégio de Santa Joana é dissolvida. Nesse mesmo ano, o mosteiro é fechado e a igreja é classificada
                como monumento nacional. O Museu de Aveiro é instalado no Convento de Jesus em 1911.

                <br>

                O Museu de Aveiro apresenta um circuito de visita com duas partes distintas mas complementares: o
                percurso monumental e a exposição permanente.


